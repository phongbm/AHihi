package com.phongbm.ahihi;

public class FriendItem {
    private String id, fullName;

    public FriendItem(String id, String fullName) {
        this.id = id;
        this.fullName = fullName;
    }

    public String getId() {
        return id;
    }

    public String getName() {
        return fullName;
    }

}